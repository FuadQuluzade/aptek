package az.orient.aptek.pojo;

public class Satish extends AbstractPojo {

    private Product product;
    private Double mebleq;
    private Integer miqdar;
    private Double totalAmount;

    public Product getProduct() {
        return product;
    }

    public void setProduct(Product product) {
        this.product = product;
    }

    public Double getMebleq() {
        return mebleq;
    }

    public void setMebleq(Double mebleq) {
        this.mebleq = mebleq;
    }

    public Integer getMiqdar() {
        return miqdar;
    }

    public void setMiqdar(Integer miqdar) {
        this.miqdar = miqdar;
    }

    public Double getTotalAmount() {
        return totalAmount;
    }

    public void setTotalAmount(Double totalAmount) {
        this.totalAmount = totalAmount;
    }

}
